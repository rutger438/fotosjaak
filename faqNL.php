<h2>FAQ</h2>
<br>
<h5>V: Wat is een schmup?</h5>
<br>
<h5>A: Een shmup is een genre van games waarin groote hoeveelheden vijanden kogels op je afschieten en jij als speler moet die ontwijken en op het zelfde moment terug schieten.</h5>
<br>
<h5>V: Ik denk dat ik geraakt word maar er gaat geen score vanaf hoe kan dit? </h5>
<br>
<h5>A: In shmup's kan alleen de middelste pixel van je karakter kan geraakt worden de rest niet.</h5>
<br>
<h5>V: Hoe download ik het spel? </h5>
<br>
<h5>A: Op de download pagina, let op je moet ingelogd zijn om deze te bekijken.</h5>
<br>
<h5>V: De game zegt game over als ik de 2e eindbaas versla!</h5>
<br>
<h5>A: De game is gebaseert op een arcade game, in arcade games staat er vaak game over als je het uitspeelt. </h5>
<br>
<h5>V: Ik heb het spel gedownload maar hij werkt niet.</h5>
<br>

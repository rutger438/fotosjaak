<h2>FAQ</h2>
<br>
<h5>Q: What is a schmup?</h5>
<br>
<h5>A: A schmup is a genre of games where large amounts of enemies fire bullets at you, you as player have to avoid these while simultaneously firing back.</h5>
<br>
<h5>Q: I think I'm being hit but nothing happens? </h5>
<br>
<h5>A: In a shmup only the center pixel of your character can be hit.</h5>
